/**
 * Fix missing images and titles for actions & ressources.
 *
 * Tasks:
 *   1. Regenerate useImages.ts from files on disk
 *   2. Scrape missing images from Prado site for actions in Prismic that lack one
 *   3. Report ressources with missing titles from Prismic
 *
 * Usage:  node scripts/fix-missing-data.mjs
 */

import { readdirSync, writeFileSync, existsSync, mkdirSync } from 'fs';
import { join, extname, basename } from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';
import * as prismic from '@prismicio/client';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const PROJECT_ROOT = join(__dirname, '..');

const PRADO_BASE = 'https://itineraires.le-prado.fr';
const RATE_LIMIT_MS = 400;

// ---------------------------------------------------------------------------
// 1. Regenerate useImages.ts from disk
// ---------------------------------------------------------------------------

function scanImagesDir(dir) {
  const map = {};
  if (!existsSync(dir)) return map;
  for (const file of readdirSync(dir)) {
    const ext = extname(file);
    if (!['.jpg', '.jpeg', '.png', '.gif', '.webp', '.svg'].includes(ext.toLowerCase())) continue;
    const name = basename(file, ext);
    const id = parseInt(name, 10);
    if (!isNaN(id)) {
      map[id] = file;
    }
  }
  return map;
}

function generateUseImages() {
  const actionsDir = join(PROJECT_ROOT, 'public/images/actions');
  const ressourcesDir = join(PROJECT_ROOT, 'public/images/ressources');

  const actionFiles = scanImagesDir(actionsDir);
  const ressourceFiles = scanImagesDir(ressourcesDir);

  const actionEntries = Object.keys(actionFiles)
    .map(Number)
    .sort((a, b) => a - b)
    .map(id => `    ${id}: "/images/actions/${actionFiles[id]}"`)
    .join(',\n');

  const ressourceEntries = Object.keys(ressourceFiles)
    .map(Number)
    .sort((a, b) => a - b)
    .map(id => `    ${id}: "/images/ressources/${ressourceFiles[id]}"`)
    .join(',\n');

  const code = `/**
 * Local image URLs for actions and ressources.
 * Auto-generated by scripts/fix-missing-data.mjs
 */
export function useImages() {
  const ACTION_IMAGES: Record<number, string> = {
${actionEntries}
  }

  const RESSOURCE_IMAGES: Record<number, string> = {
${ressourceEntries}
  }

  function getActionImage(originalId: number): string {
    return ACTION_IMAGES[originalId] ?? ''
  }

  function getRessourceImage(originalId: number): string {
    return RESSOURCE_IMAGES[originalId] ?? ''
  }

  return { getActionImage, getRessourceImage }
}
`;

  const outPath = join(PROJECT_ROOT, 'composables/useImages.ts');
  writeFileSync(outPath, code);
  console.log(`[useImages] Regenerated with ${Object.keys(actionFiles).length} actions, ${Object.keys(ressourceFiles).length} ressources`);
  return { actionIds: Object.keys(actionFiles).map(Number), ressourceIds: Object.keys(ressourceFiles).map(Number) };
}

// ---------------------------------------------------------------------------
// 2. Fetch Prismic documents and find gaps
// ---------------------------------------------------------------------------

async function fetchAllPrismicDocs(client, type) {
  const docs = [];
  let page = 1;
  let hasMore = true;
  while (hasMore) {
    const response = await client.get({
      filters: [prismic.filter.at('document.type', type)],
      pageSize: 100,
      page,
    });
    docs.push(...response.results);
    hasMore = page < response.total_pages;
    page++;
  }
  return docs;
}

// ---------------------------------------------------------------------------
// 3. Scrape missing images from Prado
// ---------------------------------------------------------------------------

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function downloadImage(imageUrl, destPath) {
  try {
    const res = await fetch(imageUrl, {
      headers: { 'User-Agent': 'Mozilla/5.0 PradoSync/2.0' },
      redirect: 'follow',
    });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    const buffer = Buffer.from(await res.arrayBuffer());
    const dir = dirname(destPath);
    if (!existsSync(dir)) mkdirSync(dir, { recursive: true });
    writeFileSync(destPath, buffer);
    return true;
  } catch (err) {
    console.log(`    [download failed] ${err.message}`);
    return false;
  }
}

function getExtFromUrl(url) {
  const match = url.match(/\.(png|jpg|jpeg|gif|webp|svg)(\?|$)/i);
  return match ? match[1].toLowerCase() : 'jpg';
}

async function scrapeImageFromDetailPage(detailUrl) {
  try {
    const res = await fetch(detailUrl, {
      headers: { 'User-Agent': 'Mozilla/5.0 PradoSync/2.0' },
      redirect: 'follow',
    });
    if (!res.ok) return null;
    const html = await res.text();

    // Try og:image first
    const ogMatch = html.match(/<meta\s+property="og:image"\s+content="([^"]*)"/);
    if (ogMatch && ogMatch[1] && !ogMatch[1].includes('placeholder')) {
      return ogMatch[1];
    }

    // Try event-image class
    const imgMatch = html.match(/<img[^>]+class="[^"]*event-image[^"]*"[^>]+src="([^"]*)"/);
    if (imgMatch) {
      const src = imgMatch[1];
      return src.startsWith('http') ? src : PRADO_BASE + src;
    }

    // Try any image in main content
    const contentImgMatch = html.match(/<img[^>]+src="(\/assets\/Uploads\/[^"]*)"/);
    if (contentImgMatch) {
      return PRADO_BASE + contentImgMatch[1];
    }

    return null;
  } catch {
    return null;
  }
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function main() {
  console.log('=== Fix Missing Data ===\n');

  // Step 1: Regenerate useImages from disk
  console.log('[Step 1] Regenerating useImages.ts from disk...');
  const { actionIds: existingActionIds, ressourceIds: existingRessourceIds } = generateUseImages();

  // Step 2: Connect to Prismic and find gaps
  console.log('\n[Step 2] Fetching Prismic documents...');
  const client = prismic.createClient('prado-nuxt');

  const actionDocs = await fetchAllPrismicDocs(client, 'action');
  const ressourceDocs = await fetchAllPrismicDocs(client, 'ressource');

  console.log(`  Prismic actions: ${actionDocs.length}`);
  console.log(`  Prismic ressources: ${ressourceDocs.length}`);

  // Find actions without images
  const actionsWithoutImage = actionDocs.filter(doc => {
    const id = doc.data.original_id;
    return id && !existingActionIds.includes(id);
  });

  // Find ressources without images
  const ressourcesWithoutImage = ressourceDocs.filter(doc => {
    const id = doc.data.original_id;
    return id && !existingRessourceIds.includes(id);
  });

  // Find ressources without titles
  const ressourcesWithoutTitle = ressourceDocs.filter(doc => {
    const title = doc.data.title;
    return !title || title.trim() === '';
  });

  console.log(`\n  Actions missing images: ${actionsWithoutImage.length}`);
  console.log(`  Ressources missing images: ${ressourcesWithoutImage.length}`);
  console.log(`  Ressources missing titles: ${ressourcesWithoutTitle.length}`);

  if (ressourcesWithoutTitle.length > 0) {
    console.log('\n[Missing titles]');
    for (const doc of ressourcesWithoutTitle) {
      const id = doc.data.original_id;
      const url = doc.data.url?.url ?? '(no url)';
      console.log(`  - ID ${id}: ${url}`);
    }
  }

  // Step 3: Scrape missing action images from Prado
  if (actionsWithoutImage.length > 0) {
    console.log(`\n[Step 3] Scraping ${actionsWithoutImage.length} missing action images from Prado...`);
    let downloaded = 0;
    for (const doc of actionsWithoutImage) {
      const id = doc.data.original_id;
      const detailUrl = doc.data.url_detail?.url;
      if (!detailUrl) {
        console.log(`  ID ${id}: no detail URL, skipping`);
        continue;
      }

      console.log(`  ID ${id}: scraping ${detailUrl}`);
      const imageUrl = await scrapeImageFromDetailPage(detailUrl);
      if (!imageUrl) {
        console.log(`    no image found`);
        await sleep(RATE_LIMIT_MS);
        continue;
      }

      const ext = getExtFromUrl(imageUrl);
      const destPath = join(PROJECT_ROOT, `public/images/actions/${id}.${ext}`);
      const ok = await downloadImage(imageUrl, destPath);
      if (ok) {
        downloaded++;
        console.log(`    saved ${id}.${ext}`);
      }
      await sleep(RATE_LIMIT_MS);
    }
    console.log(`\n  Downloaded ${downloaded}/${actionsWithoutImage.length} action images`);
  }

  // Step 3b: Scrape missing ressource images
  if (ressourcesWithoutImage.length > 0) {
    console.log(`\n[Step 3b] Scraping ${ressourcesWithoutImage.length} missing ressource images from Prado...`);
    let downloaded = 0;
    for (const doc of ressourcesWithoutImage) {
      const id = doc.data.original_id;
      const detailUrl = doc.data.url?.url;
      if (!detailUrl) {
        console.log(`  ID ${id}: no URL, skipping`);
        continue;
      }

      const fullUrl = detailUrl.startsWith('http') ? detailUrl : PRADO_BASE + detailUrl;
      console.log(`  ID ${id}: scraping ${fullUrl}`);
      const imageUrl = await scrapeImageFromDetailPage(fullUrl);
      if (!imageUrl) {
        console.log(`    no image found`);
        await sleep(RATE_LIMIT_MS);
        continue;
      }

      const ext = getExtFromUrl(imageUrl);
      const destPath = join(PROJECT_ROOT, `public/images/ressources/${id}.${ext}`);
      const ok = await downloadImage(imageUrl, destPath);
      if (ok) {
        downloaded++;
        console.log(`    saved ${id}.${ext}`);
      }
      await sleep(RATE_LIMIT_MS);
    }
    console.log(`\n  Downloaded ${downloaded}/${ressourcesWithoutImage.length} ressource images`);
  }

  // Step 4: Regenerate useImages again with new images
  console.log('\n[Step 4] Regenerating useImages.ts with newly downloaded images...');
  generateUseImages();

  // Step 5: Fix missing titles by scraping from Prado
  if (ressourcesWithoutTitle.length > 0) {
    console.log(`\n[Step 5] Attempting to fetch titles for ${ressourcesWithoutTitle.length} ressources from Prado...`);
    for (const doc of ressourcesWithoutTitle) {
      const id = doc.data.original_id;
      const url = doc.data.url?.url;
      if (!url) continue;

      const fullUrl = url.startsWith('http') ? url : PRADO_BASE + url;
      try {
        const res = await fetch(fullUrl, {
          headers: { 'User-Agent': 'Mozilla/5.0 PradoSync/2.0' },
          redirect: 'follow',
        });
        if (!res.ok) continue;
        const html = await res.text();

        // Extract title from h1 or og:title
        const h1Match = html.match(/<h1[^>]*>([^<]+)<\/h1>/);
        const ogMatch = html.match(/<meta\s+property="og:title"\s+content="([^"]*)"/);
        const title = h1Match?.[1]?.trim() || ogMatch?.[1]?.trim();

        if (title) {
          console.log(`  ID ${id}: found title "${title}"`);
          console.log(`    -> Update in Prismic manually or via migration script`);
        } else {
          console.log(`  ID ${id}: no title found on page`);
        }
      } catch {
        console.log(`  ID ${id}: fetch failed`);
      }
      await sleep(RATE_LIMIT_MS);
    }
  }

  console.log('\n=== Done ===');
}

main().catch(console.error);
